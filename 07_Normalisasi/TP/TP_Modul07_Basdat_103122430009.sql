create table MAHASISWA (
    NIM VARCHAR2(10) PRIMARY KEY,
    NAMA_MAHASISWA VARCHAR2(100),
    TANGGAL_LAHIR DATE
);

create table MATA_KULIAH (
    KODE_MK VARCHAR2(10) PRIMARY KEY,
    NAMA_MK VARCHAR2(100),
    SKS NUMBER
);

create table MASTER_BOBOT (
    NILAI VARCHAR2(2) PRIMARY KEY,
    BOBOT NUMBER
);

create table TRANSAKSI_NILAI (
    NIM VARCHAR2(10),
    KODE_MK VARCHAR2(10),
    NILAI VARCHAR2(2),
    primary key (NIM, KODE_MK),
    foreign key (NIM) REFERENCES MAHASISWA(NIM),
    foreign key (KODE_MK) REFERENCES MATA_KULIAH(KODE_MK),
    foreign key (NILAI) REFERENCES MASTER_BOBOT(NILAI)
);

