select 
    m.NAMA_MAHASISWA AS "Nama_Mahasiswa",
    m.NIM,
    TO_CHAR(m.TANGGAL_LAHIR, 'DD/MM/YY') AS "Tanggal_Lahir",
    mk.KODE_MK AS "Kode_MK",
    mk.NAMA_MK AS "Nama_MK",
    mk.SKS,
    tn.NILAI AS "Nilai",
    b.BOBOT AS "Bobot"
from TRANSAKSI_NILAI tn
join MAHASISWA m ON tn.NIM = m.NIM
join MATA_KULIAH mk ON tn.KODE_MK = mk.KODE_MK
join MASTER_BOBOT b ON tn.NILAI = b.NILAI
order by m.NIM DESC, mk.KODE_MK;